# escape chars

tabby_cat = "\tI'm tabbed in."          # tab
persian_cat = "I'm split\non a line."   # new line
backslash_cat = "I'm \\ a \\ cat."      # backslash
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""                                     # newline

puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat
